function u = laxfriedVect(N,M,fu,fx,x_start,x_end,lambda)

dx = (x_end-x_start)/N;
dt = lambda*dx;
x = dx/2:dx:x_end-dx/2;

u=[fx{1}(x); fx{2}(x)];

plot(x,u(1,:));

for n = 1:M
    
    u1=(1/2).*([u(1,2); u(2,2)]+[u(1,1); u(2,1)])...
        -(dt/(2*dx))*...
        ([fu{1}((u(1,2)),(u(2,2)));...
          fu{2}((u(1,2)),(u(2,2)))]...
        -[fu{1}((-u(1,1)),(-u(2,1)));
          fu{2}((-u(1,1)),(-u(2,1)))]);
    
    uN=(1/2).*([u(1,N); u(2,N)]+[u(1,N-1); u(2,N-1)])...
        -(dt/(2*dx))*...
        ([fu{1}((-u(1,N)),(-u(2,N)));...
          fu{2}((-u(1,N)),(-u(2,N)))]...
        -[fu{1}((u(1,N-1)),(u(2,N-1)));
          fu{2}((u(1,N-1)),(u(2,N-1)))]);
    
        u=(1/2)*([u1(1),u(1,[1:N-1]); u1(2),u(2,[1:N-1])]+[u(1,[2:N]),uN(1); u(2,[2:N]),uN(2)])...
           -(dt/(2*dx)).*...
           ([fu{1}(([u(1,[2:N]),uN(1)]),([u(2,[2:N]),uN(2)]));...
             fu{2}(([u(1,[2:N]),uN(1)]),([u(2,[2:N]),uN(2)]))]...
           -[fu{1}(([u1(1),u(1,[1:N-1])]),([u1(2),u(2,[1:N-1])]));
             fu{2}(([u1(1),u(1,[1:N-1])]),([u1(2),u(2,[1:N-1])]))]);
        
        plot(x,u(1,:));
        axis([0 200 -1 35]);
        pause(.1);
end